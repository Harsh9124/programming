// math library functions
public class seventh {
    public static void main(String[] args){
        int a,b;
        a=78;b=65;
        System.out.println(Math.max(a,b)); // returns maximum values from two numbers
        System.out.println(Math.min(a,b));
        System.out.println(Math.random()); // it return a random number b/w 0 to 1 
        System.out.println((int)Math.random()); // explicit type-casting 
        System.out.println((int)(Math.random()*100)); // 0-100 random value


    }
}
